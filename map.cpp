Map::Map(){

    river = TextureManager::LoadTexture("image/river.png");
    block = TextureManager::LoadTexture("image/block.png");
    tile = TextureManager::LoadTexture("image/tile.png");

    LoadMap(map1);

    src.x = src.y = 0;
    src.w = dest.w = 30;
    src.h = dest.h = 23;

    dest.x = dest.y = 0;

}

void Map::LoadMap(int arr[26][26]){

    for (int row = 0; row < 26;row++){
        for (int column = 0; column < 26;column++){
            map[row][column] = arr[row][column];
        }
    }

}

void Map::DrawMap(){

    int type = 0;
    for (int row = 0; row < 26;row++){
        for (int column = 0; column < 26;column++){
            type = map[row][column];
            dest.x = column * 30;
            dest.y = row * 23;

            switch (type)
            {
            case 1://磚塊
                TextureManager::Draw(tile, src, dest);
                break;
            case 2://河
                TextureManager::Draw(river, src, dest);
                break;
            case 3: // 障礙
                TextureManager::Draw(block, src, dest);
                break;
                
            
            default:
                break;
            }
        }
    }

}

